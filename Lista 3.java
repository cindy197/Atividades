// Atividade 1: 1 a 100
import java.util.Scanner;
public class Main {
    public static void main(String[] args) {


        Scanner sc = new Scanner(System.in);

        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }


    }
}

